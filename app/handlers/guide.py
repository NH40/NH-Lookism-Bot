def _guide_main_kb(back_cb: str = "settings"):
    builder = InlineKeyboardBuilder()
    buttons = [
        InlineKeyboardButton(text=label, callback_data=f"guide_section:{key}")
        for key, label in _SECTIONS
    ]
    for i in range(0, len(buttons), 2):
        builder.row(*buttons[i:i + 2])
    builder.row(InlineKeyboardButton(text="◀️ Назад", callback_data=back_cb))
    return builder.as_markup()


def _section_kb():
    builder = InlineKeyboardBuilder()
    builder.row(InlineKeyboardButton(text="◀️ К гайду", callback_data="guide"))
    return builder.as_markup()


@router.message(Command("guid"))
async def cmd_gaid(message: Message):
    await message.answer(_MAIN_TEXT, reply_markup=_guide_main_kb("main_menu"), parse_mode="HTML")


@router.callback_query(F.data == "guide")
async def cb_guide(cb: CallbackQuery):
    await cb.message.edit_text(_MAIN_TEXT, reply_markup=_guide_main_kb("settings"), parse_mode="HTML")
    await cb.answer()


@router.callback_query(F.data.startswith("guide_section:"))
async def cb_guide_section(cb: CallbackQuery):
    key = cb.data.split(":", 1)[1]
    text = _DETAILS.get(key, "❌ Раздел не найден")
    await cb.message.edit_text(text, reply_markup=_section_kb(), parse_mode="HTML")
    await cb.answer()
